#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		lst = lst -> next;
		i++;
	}
	return (i);
}
/*
lst: Le début de la liste.
Valeur de retour : Taille de la liste
Compte le nombre d’éléments de la liste.*/
